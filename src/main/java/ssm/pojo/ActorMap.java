package ssm.pojo;

public class ActorMap {

    private String rank;

    private int count;

    private double box;

    public String getRank() {
        return rank;
    }

    public void setRank(String rank) {
        this.rank = rank;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

    public double getBox() {
        return box;
    }

    public void setBox(double box) {
        this.box = box;
    }
}
